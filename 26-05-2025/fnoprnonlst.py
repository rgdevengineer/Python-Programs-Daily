#  Functional operations on a list

lst = [10,20,30,40,50]

sum_lst = sum(lst)

max_lst = max(lst)

min_lst = min(lst)

avrg_lst = sum(lst) / len(lst)

print('sum of list elements = ',sum_lst)

print('maximum element of the list = ',max_lst)

print('minimum element of the list = ',min_lst)

print('average of the list elements = ',avrg_lst)
